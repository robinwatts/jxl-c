const float *jxl_sec_half_small(size_t n) {
    switch (n) {
    case 4:
        return k_sec_half_4;
    case 8:
        return k_sec_half_8;
    case 16:
        return k_sec_half_16;
    case 32:
        return k_sec_half_32;
    default:
        return NULL;
    }
}

void jxl_sec_half_fill(size_t n, float *out_half) {
    size_t k;
    for (k = 0; k < n / 2; ++k) {
        float theta = (float)(2 * k + 1) / (float)(2 * n) * (float)M_PI;
        out_half[k] = 1.0f / (cosf(theta) * 2.0f);
    }
}

float jxl_scale_f(size_t c, size_t logb) {
    size_t idx = c << logb;
    if (idx >= 32) {
        return 1.0f;
    }
    return k_scale_f[idx];
}
